    private void btnRemoverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoverActionPerformed
        int linhaSelecionada = tblCarrinho.getSelectedRow();
        String qtd = tblCarrinho.getValueAt(linhaSelecionada,2).toString();
        int QTD = Integer.parseInt(qtd);
        String cod = tblCarrinho.getValueAt(linhaSelecionada,0).toString();
        int codP = Integer.parseInt(cod);
        
        ArrayList<Produtos> buscaNotaFProd = ProdutosDAO.listarID(codP);
        int qtdBDR = 0;
        for(Produtos item: buscaNotaFProd){
                qtdBDR   = item.getQtd();
        }
        
        int Qtd = qtdBDR + QTD;
        
        DefaultTableModel modeloCarrinho = (DefaultTableModel) tblCarrinho.getModel();
        
         
         if(linhaSelecionada >= 0){
            modeloCarrinho.removeRow(linhaSelecionada);
            Produtos alterarqtd = new Produtos();
            alterarqtd.setQtd(Qtd);
            boolean RetornoP = ProdutosDAO.alterarQTD(Qtd, codP);
         }
         else{
             JOptionPane.showMessageDialog(this,"Selecione uma linha!");
         }
         
        int rowCount = modeloCarrinho.getRowCount();
        double total = 0;
        for(int x = 0; x<rowCount; x++){
            String Valor = tblCarrinho.getValueAt(x,4).toString();
            double valor = Double.parseDouble(Valor);
            total += valor;
        }
      
        String Total = Double.toString(total);
        lblTotal.setText(Total); 
    }//GEN-LAST:event_btnRemoverActionPerformed

    private void btnAdicionarItemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAdicionarItemActionPerformed
        // Valores dos campos
        String CPF  = txtCPF.getText();
        int codP     = Integer.parseInt(txtCodProd.getText());
        int QTD     = (int) spnQTD.getValue();
        
        // Variaveis para os valores que vão vir do banco
        double precoBD   = 0;
        String MarcaBD   = null;
        int qtdBD        = 0;
        int tamBD        = 0;
        double preconota = 0;
        
        // Metodo busca
        DefaultTableModel modeloProd = (DefaultTableModel) tblCarrinho.getModel();
        if(codP != 0){
            ArrayList<Produtos> buscaNotaFProd = ProdutosDAO.listarID(codP);
            for(Produtos item: buscaNotaFProd){
                precoBD = item.getPreco();
                MarcaBD = item.getMarca();
                qtdBD   = item.getQtd();
                tamBD   = item.getTamanho();
            }
            //lblTamanho.setText(tamBD);
            if(QTD > qtdBD){
                JOptionPane.showMessageDialog(this, ("Temos apenas " +qtdBD+ " pares desse calçado em estoque"));
            }else if(QTD <= qtdBD){
                qtdBD = qtdBD - QTD;
                preconota = QTD * precoBD;
                        
                Produtos alterarqtd = new Produtos();
                alterarqtd.setQtd(QTD);
                boolean RetornoP = ProdutosDAO.alterarQTD(qtdBD, codP);

                modeloProd.addRow(new String[]{String.valueOf(codP),
                                               MarcaBD,
                                               String.valueOf(QTD),
                                               String.valueOf(preconota)
                });

                txtCodProd.setText("");
                lblMarca.setText("");
                lblModelo.setText("");
                spnQTD.setValue(1);
                lblPreco.setText("");
                lblTamanho.setText("");
            }else{
                JOptionPane.showMessageDialog(this, ("esta entrando no else do qtd"));
            }
        }else{
            JOptionPane.showMessageDialog(this, ("esta entrando no else do codp"));
        }
        
        int rowCount = modeloProd.getRowCount();
        double total = 0;
        for(int x = 0; x<rowCount; x++){
            String Valor = tblCarrinho.getValueAt(x,3).toString();
            double valor = Double.parseDouble(Valor);
            total += valor;
        }
        if(rowCount != 0){
            String Total = Double.toString(total);
            lblTotal.setText(Total);
        }
    }//GEN-LAST:event_btnAdicionarItemActionPerformed

    private void btnPesquisarCliActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPesquisarCliActionPerformed
        LocalDate localdata = LocalDate.now(); // Obtém a data atual sem o horário
        Date data = java.sql.Date.valueOf(localdata);
        // modificando para o formato dd/mm/yyyy
        SimpleDateFormat dateFormat = new SimpleDateFormat("dd/MM/yyyy");
        String dataFor = dateFormat.format(data);

        String lbl = lblNome.getText();
        
        // Pegando o valor para fazer a busca no bd
        String cpf = txtCPF.getText();
        
        // chamar a DAO
        ArrayList<Clientes> listarCarrinho = ClientesDAO.listarBusca(cpf);
        
        // Declarando as minhas variaveis
        String nome = null;
        String CPF  = null;
        int id      = 0;
        
        for(Clientes item : listarCarrinho){
            nome = item.getNome();
            CPF = item.getCpf();
            id = item.getId();
        }
        
        if(cpf != null && !cpf.replace(".", "").replace("-", "").trim().equals("")){
            lblNome.setText(nome);
            lblData.setText(dataFor);
            lblIdCli.setText(String.valueOf(id));
            lblIdCli.setVisible(false);
        }
        
        if(nome != null && !nome.isEmpty()){
            // Desabilitar o botão após clicar nele
            btnPesquisarCli.setEnabled(false);
        } else {
              JOptionPane.showMessageDialog(this, "Busque por um CPF valido!");
        }

    }//GEN-LAST:event_btnPesquisarCliActionPerformed

    private void btnPagamentoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPagamentoActionPerformed
        LocalDate localdata = LocalDate.now(); // Obtém a data atual sem o horário
        Date data = java.sql.Date.valueOf(localdata);
        ArrayList<ItemNota> lista = new ArrayList<>();
        
        // Chamei a tabela e vou inserir os produtos
        DefaultTableModel Carrinho = (DefaultTableModel) tblCarrinho.getModel();
        int linhastabela = Carrinho.getRowCount();
        
        // pegando os valores que vou guardar na tabela venda relatório simples
        String totaltext  = lblTotal.getText();
        double total      = Double.parseDouble(totaltext);
        int QTD           = 0;
        String idtext     = lblIdCli.getText();
        int idC            = Integer.parseInt(idtext);
        
        //Somando a quantidade de todos os produtos
        for(int x=0; x< linhastabela; x++){
            String qtdT = tblCarrinho.getValueAt(x, 2).toString();
            int QTDT = Integer.parseInt(qtdT);
            QTD = QTD + QTDT;
        }
        
        // Chamando o método para fazer o primeiro insert
        NotaFiscal venda = new NotaFiscal(total, QTD, data, idC);
        
        if(linhastabela != 0){
            int linhatab  = Carrinho.getRowCount();
            
            for(int x = 0; x< linhatab; x++){
                String idPS = tblCarrinho.getValueAt(x, 0).toString();
                String marca = tblCarrinho.getValueAt(x, 1).toString();
                String qtd = tblCarrinho.getValueAt(x, 2).toString();
                String precoS = tblCarrinho.getValueAt(x, 3).toString();
                
                int QTDP = Integer.parseInt(qtd);
                double PrecoD  = Double.parseDouble(precoS);
                int idProdInt  = Integer.parseInt(idPS);
                
                ItemNota itemNota = new ItemNota();
                itemNota.setIdProduto(idProdInt);
                itemNota.setMarca(marca);
                itemNota.setVlrProduto(PrecoD);
                itemNota.setQtdProduto(QTDP);
                
                lista.add(itemNota);
                        
                venda.setListaItens(lista);  
            }
            
            boolean retorno = NotaFiscalDAO.salvar(venda);
        
            if (retorno) {
                JOptionPane.showMessageDialog(null, "Cadastro efetuado com sucesso na tabela venda!");
            } else {
                JOptionPane.showMessageDialog(null, "Erro ao fazer o cadastro na tabela venda!");
            } 
            btnPesquisarCli.setEnabled(true);
            lblNome.setText("");
            lblData.setText("");
            txtCPF.setText("");
            txtCodProd.setText("");
            lblMarca.setText("");
            lblModelo.setText("");
            spnQTD.setValue(1);
            lblTotal.setText("");
            lblPreco.setText("");
            lblTamanho.setText("");
            Carrinho.setRowCount(0);
        }else{
                JOptionPane.showMessageDialog(null, "Escolha um produto!");
            }
    }//GEN-LAST:event_btnPagamentoActionPerformed

