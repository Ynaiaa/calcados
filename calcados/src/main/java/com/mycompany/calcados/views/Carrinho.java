    private void btnCancelarVendaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCancelarVendaActionPerformed
        btnPesquisarCli.setEnabled(true);
        lblNome.setText("");
        lblData.setText("");
        txtCPF.setText("");
        txtCodProd.setText("");
        lblMarca.setText("");
        lblModelo.setText("");
        spnQTD.setValue(1);
        lblTotal.setText("");
        lblPreco.setText("");
        lblTamanho.setText("");
        
        DefaultTableModel modeloProdu = (DefaultTableModel) tblCarrinho.getModel();
        int contalinhas = modeloProdu.getRowCount();
        
        for(int x = contalinhas - 1; x >= 0; x--){
            // Pego os valores
            String qtd = tblCarrinho.getValueAt(x,2).toString();
            int QTD = Integer.parseInt(qtd);
            String cod = tblCarrinho.getValueAt(x,0).toString();
            int codP = Integer.parseInt(cod);
            
            // Chamo o metodo para buscar a qtd no bd
            ArrayList<Produtos> buscaNotaFProd = ProdutosDAO.listarID(codP);
            int qtdBDR = 0;
            for(Produtos item: buscaNotaFProd){
                    qtdBDR   = item.getQtd();
            }
            
            // Soma o que foi retirado
            int Qtd = qtdBDR + QTD;
            
            // Adiciono novamente a qtd no bd e excluo a linha
            Produtos alterarqtd = new Produtos();
            alterarqtd.setQtd(Qtd);
            boolean RetornoP = ProdutosDAO.alterarQTD(Qtd, codP);
            modeloProdu.removeRow(x);
        }  
    }//GEN-LAST:event_btnCancelarVendaActionPerformed

