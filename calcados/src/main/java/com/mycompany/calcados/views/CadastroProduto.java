    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        int linhaSele = tblProduto.getSelectedRow();
  
        DefaultTableModel modeloProd = (DefaultTableModel) tblProduto.getModel();
        
        if(linhaSele != -1){
            String id = tblProduto.getValueAt(linhaSele, 0).toString();
            int Id    = Integer.parseInt(id);
            
            boolean RetornoP = ProdutosDAO.excluirP(Id);


            if(RetornoP){
                 modeloProd.removeRow(linhaSele);
            }
            else{
                JOptionPane.showMessageDialog(this,"Selecione uma linha!");
            }
        }else{
            JOptionPane.showMessageDialog(this, "Selecione uma linha!");
        }
    }//GEN-LAST:event_btnExcluirActionPerformed

