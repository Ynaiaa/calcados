    private void btnAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAlterarActionPerformed
       LocalDate localdata = LocalDate.now(); // Obtém a data atual sem o horário
       Date data = java.sql.Date.valueOf(localdata);
       // modificando para o formato dd/mm/yyyy
       SimpleDateFormat dateFormat = new SimpleDateFormat("dd/MM/yyyy");
       String dataFor = dateFormat.format(data); 
       int linhaSele  = tblProduto.getSelectedRow();
       
       DefaultTableModel modeloP = (DefaultTableModel) tblProduto.getModel();
 
       // pegando os valores das colunas
       String cod = tblProduto.getValueAt(linhaSele,0).toString();
       String marca = tblProduto.getValueAt(linhaSele,1).toString();
       String modelo = tblProduto.getValueAt(linhaSele,2).toString();
       String cor = tblProduto.getValueAt(linhaSele,3).toString();
       String genero = tblProduto.getValueAt(linhaSele,4).toString();
       String tamanho = tblProduto.getValueAt(linhaSele,5).toString();
       String preco = tblProduto.getValueAt(linhaSele,6).toString();
       String Qtd = tblProduto.getValueAt(linhaSele,7).toString();
        
       int qtd  = Integer.parseInt(Qtd);
       int Cod  = Integer.parseInt(cod);
       int Tamanho  = Integer.parseInt(tamanho);
       double Preco  = Double.parseDouble(preco);
        
       //String Valor = tblClientes.getValueAt(linhaSelecionada,colunaSelecionada).toString();
        
       Produtos prodAlter = new Produtos();
       prodAlter.setCod_prod(Cod);
       prodAlter.setPreco(Preco);
       prodAlter.setTamanho(Tamanho);
       prodAlter.setModelo(modelo);
       prodAlter.setMarca(marca);
       prodAlter.setCor(cor);
       prodAlter.setGenero(genero);
       prodAlter.setData(data);
       prodAlter.setQtd(qtd);
       
       boolean RetornoP = ProdutosDAO.alterarP(prodAlter);
        
        if(RetornoP){
            // remove a linha da tabela
            modeloP.removeRow(linhaSele);
            JOptionPane.showMessageDialog(rootPane, "Alterado com sucesso!");
            
        }else{
              JOptionPane.showMessageDialog(rootPane, "Falha ao alterar!");
        }
    }//GEN-LAST:event_btnAlterarActionPerformed

    
