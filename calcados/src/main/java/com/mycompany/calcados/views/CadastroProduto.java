    private void btnInserirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInserirActionPerformed
       LocalDate localdata = LocalDate.now(); // Obtém a data atual sem o horário
       Date data = java.sql.Date.valueOf(localdata);
       
       // Guardo o valor em variaveis para passar ao meu obj
       int CodProd      = Integer.parseInt(txtCodProd.getText());
       int Tamanho      = Integer.parseInt(txtTamanho.getText());
       double preco     = Double.parseDouble(txtPreco.getText());
       String Modelo    = txtModelo.getText();
       String Cor       = txtCor.getText();
       int qtd     = (int) spnQtd.getValue();
       
       String Marca = cboMarca.getSelectedItem().toString();
       String Genero = cboGenero.getSelectedItem().toString();
       
       if(Marca == "Selecione uma opção" || Genero == "Selecione uma opção"){
           if(Marca == "Selecione uma opção"){
               JOptionPane.showMessageDialog(null, "Selecione uma opção de Marca");
           }else{
               JOptionPane.showMessageDialog(null, "Selecione uma opção de Genero");
           }
       }else if(qtd == 0 || preco == 0 || Tamanho == 0){
           if(qtd == 0){
               JOptionPane.showMessageDialog(null, "Preencha a quantidade de produtos!");
           }else if(Tamanho == 0){
               JOptionPane.showMessageDialog(null, "Especifique o tamanho do produtos!");
           }else{
               JOptionPane.showMessageDialog(null, "Digite o preço do produto!");
           }
       }else if(Modelo.isEmpty() || Cor.isEmpty()){
           if(Modelo.isEmpty()){
               JOptionPane.showMessageDialog(null, "Preencha o modelo do produto!");
           }else{
               JOptionPane.showMessageDialog(null, "Preencha a cor do produto!!");
           }
       }else{
            Produtos CadProdutos = new Produtos(CodProd, preco, Tamanho, Modelo, Marca, Cor, Genero, data, qtd);
            boolean RetornoP = ProdutosDAO.cadastrarP(CadProdutos);
       
            if (RetornoP) {
                JOptionPane.showMessageDialog(null, "Cadastro efetuado com sucesso!");
                txtCodProd.setText("");
                txtTamanho.setText("");
                txtPreco.setText("");
                txtModelo.setText("");
                txtCor.setText("");
                spnQtd.setValue(0);
                cboMarca.setSelectedItem("Selecione uma opção");
                cboGenero.setSelectedItem("Selecione uma opção");

            } else {
                JOptionPane.showMessageDialog(null, "Erro ao fazer salvar o cadastro!");
            }
        }
    }//GEN-LAST:event_btnInserirActionPerformed

    private void txtCodProdKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtCodProdKeyTyped
           char c =evt.getKeyChar();
        
            // se for qualquer tecla diferente de a 9 e backspace
            if((c<'0' || c>'9') && (c != KeyEvent.VK_BACK_SPACE)){
                evt.consume();
                JOptionPane.showMessageDialog(this,"Digite apenas números no Codigo do Produto");
        }
            
        if(txtCodProd.getText().length()>4){
            evt.consume();
            JOptionPane.showMessageDialog(this,"Máximo de 5 caracteres no Codigo do Produto");
        }
    }//GEN-LAST:event_txtCodProdKeyTyped

